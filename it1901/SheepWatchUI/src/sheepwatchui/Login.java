/**
 * Method called when the user clicks the Login button.
 * Checks if the username and password is correct, and logges the user in if
 * the information is correct.
 * @param evt 
 */
    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        if (checkUser()) {
            labInfoLogin.setText("Logger inn...");
            labInfoLogin.setForeground(java.awt.Color.BLUE);
            new MainPage(currentFarmer).setVisible(true);
            setVisible(false);
            dispose();
        } else {
            labInfoLogin.setText("Feil brukernavn og/eller passord");
            labInfoLogin.setForeground(java.awt.Color.RED);
        }
    }//GEN-LAST:event_btnLoginActionPerformed
