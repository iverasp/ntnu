    /**
     * Checks if the username and password is correct.
     * @return true if the user successfully logged in
     */
    private boolean checkUser() {
        Farmer farmer = DBConnector.checkUser(txtEmail.getText(), txtPassword.getText());
        if(farmer != null){
            currentFarmer = farmer;
            return true;
        }else{
            return false;
        }
    }
    
